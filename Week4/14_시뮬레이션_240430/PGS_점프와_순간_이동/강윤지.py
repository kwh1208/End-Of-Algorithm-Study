# 아이디어
## N 부터 시작해서 짝수일 경우 2로 나누고, 홀수일 경우 1을 빼는 것을 0기 될 때 까지 반복
## 5000 -> 2500 -> 1250 -> 625 -> 624 -> 312 -> 156 -> 78 -> 39 -> 38 -> 19 -> 18 -> 9 -> 8 -> 4 -> 2 -> 1 -> 0

# 예상 시간복잡도
## O(logN)

# 풀이시간 
## 20분

# 실행시간 
## 0.00ms~ 0.03ms

def solution(n):
    ans = 0

    while n != 0:

        # 홀수이면 1 감소
        if n % 2:
            n-=1
            ans+=1
        else:
            n /= 2

    return ans